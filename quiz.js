function show(count) {
  let questionContainer = document.getElementById("questions");
  let currentQuestion = questions[count];

  // Create question element
  let questionElement = document.createElement("div");
  questionElement.classList.add("question");

  // Add question text
  let questionText = document.createElement("p");
  questionText.textContent = `${currentQuestion.question}`;
  questionElement.appendChild(questionText);

  // Add image if available
  if (currentQuestion.image) {
    let imageElement = document.createElement("img");
    imageElement.src = currentQuestion.image;
    questionElement.appendChild(imageElement);
  }

  // Check question domain
  switch (currentQuestion.domain) {
    case "Vocabulary":
    case "Arithmetic":
    case "Spatial Reasoning":
      // MCQs - Add options
      let optionsList = document.createElement("ul");
      optionsList.classList.add("option_group");

      currentQuestion.options.forEach((option) => {
        let optionItem = document.createElement("li");
        optionItem.classList.add("option");
        optionItem.textContent = option;
        optionsList.appendChild(optionItem);
      });

      questionElement.appendChild(optionsList);
      break;

    case "Creative Expression":
      // Short answer type - No options, only answer input field
      let answerInput = document.createElement("input");
      answerInput.setAttribute("type", "text");
      answerInput.setAttribute("placeholder", "Type your answer here");
      answerInput.classList.add("creative_input"); // Add class for styling
      questionElement.appendChild(answerInput);

      // Convert user input to lowercase when input changes
      answerInput.addEventListener('input', function() {
        this.value = this.value.toLowerCase();
      });

      break;

    case "Personality":
    case "Emotional Awareness":
      // Value-based - Display value associated with each option
      let valueOptionsList = document.createElement("ul");
      valueOptionsList.classList.add("option_group");

      currentQuestion.options.forEach((option) => {
        let optionItem = document.createElement("li");
        optionItem.classList.add("option");
        optionItem.textContent = option.value; // Display value
        valueOptionsList.appendChild(optionItem);
      });

      questionElement.appendChild(valueOptionsList);
      break;
  }

  questionContainer.innerHTML = ""; // Clear previous question
  questionContainer.appendChild(questionElement);

  toggleActive();
}
function toggleActive() {
  let option = document.querySelectorAll("li.option");
  for (let i = 0; i < option.length; i++) {
    option[i].onclick = function() {
      for (let j = 0; j < option.length; j++) {
        if (option[j].classList.contains("active")) {
          option[j].classList.remove("active");
        }
      }
      option[i].classList.add("active");

      // Get the selected option's text content
      let selectedOption = option[i].textContent.trim();

      // Determine the domain of the current question
      let currentQuestion = questions[question_count];
      let domain = currentQuestion.domain;

      // Check if the selected option is correct
      let isCorrect = selectedOption === currentQuestion.answer;

      // Calculate score based on the domain and selected option
      switch (domain) {
        case "Vocabulary":
        case "Arithmetic":
        case "Spatial Reasoning":
          // For these domains, each correct answer earns a percentage of the total score
          if (isCorrect) {
            if (domain === "Vocabulary") {
              VocabularyScore += (10 / totalQuestions[domain]) * 100;
            } else if (domain === "Arithmetic") {
              ArithmeticScore += (10 / totalQuestions[domain]) * 100;
            } else if (domain === "Spatial Reasoning") {
              SpatialReasoningScore += (10 / totalQuestions[domain]) * 100;
            }
          }
          break;
        case "Creative Expression":
          // For this domain, check user input and calculate score
          let userInput = document.querySelector(".creative_input").value.trim();
          if (userInput === currentQuestion.answer) {
            CreativeExpressionScore = 10;
          } else {
            CreativeExpressionScore = 0;
          }
          break;
      }
    }
  }
}
    
let timer; // Define timer variable globally

function startTimer() {
  let timeInSeconds = 30; // Set the timer duration in seconds
  let timerDisplay = document.getElementById('timer');

  // Update the timer display every second
  timer = setInterval(function() {
    timerDisplay.textContent = timeInSeconds;
    timeInSeconds--;

    // If time runs out, move to the next question
    if (timeInSeconds < 0) {
      clearInterval(timer);
      next(); // Move to the next question
    }
  }, 1000); // Update every second (1000 milliseconds)
}

function next() {
  // Reset the timer (make sure 'timer' is declared somewhere in your code)
  clearInterval(timer);

  // Check if it's the last question
  if (question_count == questions.length - 1) {
    // Calculate total score
    totalScore = (ArithmeticScore + VocabularyScore + SpatialReasoningScore + RealisticScore + InvestigativeScore + ArtisticScore + SocialScore + EnterprisingScore + ConventionalScore + CreativeExpressionScore + SelfAwarenessScore + SelfManagementScore + SocialAwarenessScore + RelationshipManagementScore) / 15;

    // Redirect to the final page or display scores
    location.href = "final.html";
  } else {
    // Move to the next question
    question_count++;

    // Reset timer before displaying the next question
    startTimer();

    // Display the next question
    show(question_count);
    
    // If it's the second last question, hide the next button and show the submit button
    if (question_count == questions.length - 1) {
      // Hide the next button
      document.querySelector(".btn-next").style.display = "none";
      
      // Get the question container element
      let questionContainer = document.getElementById("questions");
      
      // Create a submit button element
      let submitButton = document.createElement("button");
      submitButton.textContent = "Submit";
      submitButton.classList.add("submit-btn"); // Add the CSS class to style the button
      submitButton.addEventListener("click", function() {
        // Redirect to the final page when the button is clicked
        location.href = "final.html";
      });
      
      // Add the submit button to the question container
      questionContainer.appendChild(submitButton);
    }
    // After calculating scores, store them in localStorage
localStorage.setItem('VocabularyScore', VocabularyScore);
localStorage.setItem('ArithmeticScore', ArithmeticScore);
// Store other scores similarly
  }

}
